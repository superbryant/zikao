2、垃圾焚烧烟气处理技术。 
3、废液废渣处理技术。 
（三）生物质气化发电技术 
生物质气化发电技术的基本原理是把生物质转化为可燃气，再利用可燃气燃烧发电。生物质气化发电过程主要包括三部分： 
1、生物质气化，把固体生物质转化为气体燃料。 
2、将燃料气化净化，将燃气中的杂质脱除出去，以保证燃气发电设备的正常运行。 
3、燃气发电，利用燃气轮机或燃气内燃机进行发电，有的工艺为了提高能源利用率，燃气轮机发电之后增加余热锅炉和蒸汽轮机提高能源利用率。 
第九章  地热能 
一．地热 
地热的主要来源：地球物质中放射性元素衰变产生的热量是地热的主要来源。 
地热资源类型（五种）：【简答题】 
（一）蒸汽型地热资源。 
（二）热水型地热资源。 高温超过150℃，中温90-150℃和低温不足90℃。高温地热资源优先用于发电。 
（三）地压型地热资源。 
（四）干热岩型地热资源。 
（五）岩浆型地热资源。 
二．地热发电系统的类型【只本科考】 
根据地热资源的特点以及开发技术不同，地热发电系统主要有以下5 类：【简答题】 
（一）地热蒸汽发电系统（投资少、系统最简单、运行安全可靠、经济性也高，是地热发电的主要形式）。 
（二）闪蒸地热发电系统（开发普遍的是储量相对较多、分布较广的热水型地热资源，其热能产生形式是热水或湿蒸汽，比较适合采用闪蒸地热发电系统）。 
（三）双循环地热发电系统。 
（四）全流式地热发电系统。 
（五）干热岩地热发电系统。 
三．地源热泵【简答题】 
地源热泵是一种利用地下浅层地热资源的既可供热又可制冷的高效节能空调系统。 
通常地源热泵消耗1kw的能量，用户可以得到4kw以上的热量或冷量。与锅炉供热系统相比，地源热泵要比电锅炉加热节省2/3以上的电能，比燃料锅炉节省1/2以上的能量。地源热泵的热源温度全年较为稳定，一般为10-25℃，其制冷、制热系数可达3.5-4.4，与传统的空气源热泵相比，要高出40%左右，其运行费用为普通中央空调的50%-60%。 
地源热泵特点：【1704专简答题】 
1、属可再生能源利用技术。 
2、属经济有效的节能技术。 
3、环境效益显著。 
4、一机多用，应用范围广。 
地源热泵的工作原理：【1804本简答题】 
地源热泵是利用水源热泵的一种形式，它与利用水与地能（地下水、土壤或地表水）进行冷热交换来作为水源热泵的冷热源，冬季把地能中的热量“取”出来，供给室内采暖，此时地能为“热源”；夏季把室内热量取出来，释放到地下水、土壤或地表水中，此时地能为“冷源”。地源热泵供暖空调系统主要分三部分；室外地能换热系统、水源热泵机组和室内采暖空调末端系统。 
地源热泵的应用：【简答题】 
（一）家用系统 
（二）集中系统 
（三）分散系统 
（四）混合系统 
（五）水环路热泵空调系统（节省占地、能源费用单独计量、调节灵活、灵活应用） 
    目前，我国地热直接利用的年产出能量和设备总装机容量分别在世界排名第一位和第二位。 
四．地热直接利用 
地热能的直接利用技术要求较低，所需设备也较容易。目前对地热的直接利用发展十分迅速，已广泛地用于工业加工、民用采暖和空调、洗浴、医疗、农业温室、农田灌溉、土壤加温、水产养殖、畜禽饲养等各个方面，收到了良好的经济技术效益，减轻了环境污染，节约了能源。 
将地热能直接用于采暖、供热和供热水是仅次于地热发电的地热利用方式。 
