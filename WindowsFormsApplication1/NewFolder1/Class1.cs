3.用电负荷计算 
电气负荷计算方法有需要系数法、利用系数法、二项式系数法、单位面积功率计算法、单位产品功率计算法等。【简答题】 
（1）需要系数法。用设备功率乘以需要系数和同时系数，直接求出计算负荷。【计算题】 
（2）利用系数法。采用利用系数求出最大负荷班的平均负荷，再考虑设备台数和功率差异的影响，乘以与有效台数有关的最大系数求得计算负荷。 
（3）二项式系数法。将负荷分为基本负荷和附加负荷，后者考虑一定数量大容量设备影响。 
（4）单位面积功率法、单位指标法、单位产品耗电量法等，可用于初步设计用电量指标的估算，对于住宅建筑，在设计各阶段均可使用单位面积功率法。 
上述各计算方法的应用范围各不一样，按《民用建筑电气设计规范》，“在方案设计阶段可采用单位指标法；在初步设计阶段，宜采用需要系数法”。可见民用建筑电气计算负荷推荐采用需要系数法。 
三.供配电系统 
用户供配电系统通常是由降压变电所、配电所、电力线路和电能用户组成的电力终端系统。一般中小型用户的电源进线电压为6~10kV，大中型用户的电源进线电压为35kV及以上，有些小型用户则采用低压进线。 
供配电系统接线方式【只本科考】 
（一） 高压配电线路的接线方式 

1. 放射式接线 


特点：一回高压配电线路只向一个地点送电，各回高压配电线路之间没有共线。 
优点：可靠性较高，一回高压配电线路因故障而停电时，仍能正常供电。 
缺点：增加了投资。 
2. 树干式接线 


特点：备受电单元容量不是太大，位置相对集中，距电源端相对较远，共用一回高压配电线路进电。 
优点：节省投资。 
缺点：供电可靠性较低。 
3. 环形接线 


特点：任何一个受电单元高压母线都设置了两端电源进线，环形接线上的受 
电单元采用的这种高压开关柜被称为环网柜。实质上等效于两端供电的树干式接线方式。 
（二）低压配电线路的接线方式：放射式接线、树干式接线、环形接线。 


四.供配电系统的质量调节方式 
（一）供配电系统的质量调节方式【简答题】 
1、供配电系统的电压调整。 
电压偏差或称电压偏移，是指给定瞬间设备的端电压U与设备额定电压UN 之差，通常用它对额定电压的的百分比来表示。 
2、电压波动及其抑制。【电压波动是由于负荷急剧变动的冲击性负荷所引起的。】 
3、电网高次谐波及其抑制。 
4、提高功率因数。 
（二）抑制电网谐波的措施【简答题】 
（1）三相整流变压器采用Y，d或D，y接线； 
（2）增加整流变压器二次侧的相数； 
（3）使各台整流变压器二次侧互有相角差； 
（4）装设分流滤波器； 
（5）选用D，ynll连接组别的三相配电变压器； 
（6）其他措施。限制电力系统中接入的交流设备及交流调压装置等的容量r或提高对大容量非线性设备的供电电压等。 
（三）提高功率因数 
提高功率因数好处：【简答题】 
1、提高设备的利用率。 
2、减少供配电系统的电压损失。 
3、减少系统的有功功率损耗。 
4、提高供配电系统的传输能力。 
针对工业企业中无功功率主要消耗在电动机、变压器上的特点，为提高自然功率因数，常采取以下措施：【简答题】 
1、提高感应电动机检修质量，防止定子与转子间气隙过大。 
2、合理选择电动机、变压器容量，使其尽量在接近最佳负荷率下运行。电动机接近满载，变压器负荷率在75%左右较合适。 
3、采用技术措施，降低轻载设备的外加电压，如将△接线的电动机改为Y接线。或者切断空载设备电源，如电焊机空载自停装置等。 
4、绕线型感应电动机同步化运行。 
5、负荷联接应力求三相平衡。 
五．变压器 
变压器是由一个铁芯和两个或两个以上绕组组成的静止感应电器，根据电磁感应原理，把某一电压、电流的交流电能变换成同频率的另一电压、电流的电能，以达到传输电功率和进行电气隔离的目的。 
常用变压器的类型及特点： 
1、按冷却方式分：干式变压器、油浸式变压器、氟化物变压器。 
2、按防潮方式分：开放式变压器、灌封式变压器、密封式变压器。 
3、按铁芯形式分：芯式变压器、壳式变压器、环形变压器、金属箔变压器、非晶合金变压器。 
4、按电源相数分：单相变压器、三相变压器、多相变压器。 
5、按用途分：电源变压器、调压变压器、音频变压器、中频变压器、高频变压器、脉冲变压器。 
变压器的合理选择原则：【只本科考】【简答题】 
（一）变压器容量越小，效率越差，对于负荷较稳定的情况，应选择台数尽量少的大容量变压器，而不应选择多台小容量的变压器。 
（二）视在功率计算负荷的数值在两个标准容量等级之间时，如近期内负荷会增长，则应选大一级容量的变压器。如近期内负荷不会增加，则可选择小一级容量。 
（三）对于电力使用地点分散，且相隔遥远的情况下，由于电路损失和电压降增大，可以考虑选择多台变压器分散设置。 
（四）四压器的组合。 
（五）对于用电负荷随季节性变化较大时，应考虑选择多台变压器并联运行的方案。 
变压器并联运行应同时满足的条件：【简答题】 
（一）变压器的接线组别相同。 
（二）变压器的变比相同。 
（三）变压器的短路电压相等。 
六．电机与用电设备 
1.电动机 
电动机是一种旋转式电动机器，它将电能转变为机械能，它主要包括一个用以产生磁场的电磁铁绕组或分部的定子绕组和一个旋转电枢或转子。 
Y-表示异步电动机；T-表示同步电动机。 
功率：表示额定运行时电动机轴上输出的额定机械功率，单位kW 或HP，1HP=0.736kW。 
   【只本科考】三相异步电动机：定子绕组接入三相交流电源后，绕组电流产生的旋转磁场在转子导体中产生感应电流，转子在感应电流和气隙旋转磁场的相互作用下，又产生电磁转矩（即异步转矩），使电动机旋转。 
2.泵与风机 
    泵与风机是一种利用外加（原风机）能量输送流体的机械。通常将输送液体的机械称为泵，输送气体的机械称为风机。 
（一）泵与风机的分类 
1、按工作原理分类：叶片式（离心式、轴流式、混流式，旋涡泵）、容积式（活塞泵、柱塞泵、隔膜泵和空气压缩机）、其他形式（喷射泵、水机泵）。 
2、按产生的压头分类： 
（1）泵按产生的压头可以分为低压泵、中压泵、高压泵。 
（2）风机按产生的压头可以分为：通风机、鼓风机、压气机。 
3、按生产中的作用分类：水泵、凝结水泵、循环水泵、主油泵、疏水泵、灰渣泵、送风机、引风机、排粉风机等。 
（二）泵与风机的性能参数 
1、流量：指单位时间内泵与风机输送流体的数量。 
2、扬程或全压：单位重力作用下流体通过泵或风机后的能量增加值。 
3、功率（通常所说的功率是轴功率，又称输入功率：原动机传到泵与风机轴上的功率）。 
有效功率是指单位时间内通过泵或风机的流体所获得的功，即泵与风机的输出功率， 
用Pe 表示，单位为kW。有效功率可由泵与风机的输出流量及扬程或全压求得。 
轴功率即原动机传到泵与风机轴上的功率，又称输入功率，用P 表示，单位为kW 。 
轴功率通常由电测法确定，即用功率表测出原动机输入功率。 
4、效率（指泵与风机输出功率与输入功率之比的百分数）。效率的实质是反映泵或风机在传递能量过程中轴功率被有效利用的程度。 
5、转速：泵与风机叶轮每分钟的转数，水泵还有允许吸上真空高度或允许气蚀余量等。 
