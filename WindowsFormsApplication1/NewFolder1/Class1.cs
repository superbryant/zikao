第二章  石油、天然气 
一．石油 
1.石油 
（一）石油的组成 
    石油又称“原油”，是仅次于煤的化石燃料，它是一种天然的，黄色、褐色或黑色的，流动或半流动的，黏稠的可燃液体烃类混合物。 
石油主要是由烷烃、环烷烃、芳香烃等烃类化合物组成。 
石油的主要元素是碳（85%～90%）和氢（10%～14%），还有少量的硫（0.2%～0.7%）、氧（0～1.5%）、氮（0.1%～2%）以化合物、胶质、沥青质等非烃类物质形态存在。 
（二）石油的分类 
通常市场上有三种分类方法： 
1、按石油的密度分类：轻质石油、中质石油、重质石油、特重质石油。 
2、按石油中的硫的含量分类：小于0.5%的为低硫石油、0.5%～2.0%的为含硫石油、大于2.0%的称高硫石油。世界石油总产量中，含硫石油和高硫石油约占75% 。 
3、按石油中的蜡含量分类：0.5%～2.5%的为低蜡石油、2.5%～10%的为含蜡石油、大于10%为高蜡石油。 
2.石油的加工 
根据所需产品的不同，炼油厂的加工流程大致分为三种类型： 
（一）燃料型（以汽油、煤油、柴油等燃料油为主要产品）。 
（二）燃料-润滑油型（除生产燃料油外，还生产各种润滑油）。 
（三）石油化工类（提供石脑油、轻油、渣油用作生产石油化工产品的原料）。 
石油炼制的方法分为：分离法（如溶剂法、固体吸附法、结晶法和分馏法等，其中最常用的是分馏法。）和转化法（常用的转化法有热裂化、催化裂化、加氢裂化和焦化等）。 
3.主要石油产品 
按石油的用途和特性，可将其分成14 大类。其主要石油产品的用途： 
（一）溶剂油（橡胶、油漆、油脂、香料、药物等）。 
（二）燃料油（汽车、飞机）。 
（三）润滑油（汽油发动机、缝纫机）。 
（四）润滑脂（润滑机械）。 
（五）石蜡和地蜡（火柴、蜡烛）。 
（六）沥青（工程防水、铺路）。 
（七）石油焦（制造电极）。 
4.燃料油的主要性能指标 
燃料油为家用或工业燃烧器上用的液体燃料。燃料油的主要性能指标包括黏度、含硫量、低温性能和安定性等。黏度是燃料油的重要指标，黏度过高会导致燃料油的雾化性能恶化，使喷出的油滴过大，造成燃烧不完全，热效率下降。粘度（以其100℃时的运动粘度为指标）、含硫量（1 号和2 号燃料油质量指标中规定其含硫量不得大于0.5%）、低温性能（通常用倾点来评定，1 号燃料油倾点不高于-18℃，4 号轻和4 号燃料油倾点不高于-6℃）和安定性（主要是对高粘度燃料油而言的）。 
二．天然气基本知识 
1.天然气 
天然气是除煤和石油之外的另一种重要的一次能源。它燃烧时有很高的发热值，对环境的污染也小，而且还是重要的化工原料。天然气的生成过程同石油类似，但它比石油更容易生成。天然气主要由甲烷、乙烷、丙烷和丁烷等烃类组成，其中甲烷占80%~90%。通常天然气可以分为纯天然气、石油伴生气、凝析气和矿井气4种。纯天然气是从地下气层矿井中开采出来的干天然气，也称气田气，它的特点是甲烷（CH4)含量高,占75%~98%，含乙烷（C2H6)及其他重碳氢化合物为1%~10%，二氧化碳（CO2）含量0~1.0%，其他成分主要是氮（N2）1.0%~9.0%，而硫化氢含量甚微，仅个别气田的含硫量可能较高。气田气从地下引出时，通常具有很高的压力，便于直接输出。石油伴生气是开采石油时的副产品，是石油开采过程中从油井获得的可燃气体，也称油田气或湿气。油田气含甲烷较低，我国油田气中甲烷含量一般为30%~70%，含乙烷、丙烯及重碳氢化物的总含量可达10%~20%，而氮气含量可高达2.32%，二氧化碳含量一般为5%左右，硫化氢含量在3%左右，含硫量则视产地而异，一般几乎不含硫。油田气随石油引出井外，经分离装置分离出来时，其压力较低，不便直接输送，但可经加压后转化为液态，以便输送或储存。矿井气又称煤层气，是伴随煤矿开采而产生的，俗称瓦斯。通常60%的天然气为气田气，40%的为伴生气，煤层气则可能附于煤层中或另外聚集，在7~17MPa和40~70℃时每吨煤可吸附13~30m3的甲烷。 
热值从高到低的顺序：油田伴生气>干井天然气>矿井气。 
