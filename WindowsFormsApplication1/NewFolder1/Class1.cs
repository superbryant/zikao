（三）活塞空压机与螺杆空压机的区别【简答题】 
1、螺杆式空压机与活塞式空压机相比较，它不存在往复惯性力和力矩，所以转速高、基础小、重量轻、振动小、运转平稳。 
2、它是无活塞机中的活塞和高频振动的进排气阀，故零部件少、结构简单易于维修。 
3、在转子没转之内常有多次排气过程，所以它输气均匀、压力脉动小，不必设置大容量的溶气罐。 
4、它的转子和星轮片的加工困难需要专用设备，并且相对运动的机件之间密封。 
5、由于转速高，加之工作容积与吸、排气孔口周期性地通断产生较为强烈的空气动力噪声，需采取特殊的减噪消声设置。 
5.制冷压缩机 
    制冷压缩机无论在制冷系统还是在热泵系统中，它的功能没有任何区别，最根本的不同是各自工作温度范围不同。制冷系统是从被冷却介质吸热而向环境介质排热，其压缩机运行工况中的蒸发温度要低于被冷却介质温度，冷凝温度要高于环境介质温度。对热泵系统，以空气源热泵为例，是从大气吸热，并向温度较高的温度区域供热，于是，其压缩机运行工况中的蒸发温度要低于大气温度，冷凝温度要高于供热区域的温度。 
