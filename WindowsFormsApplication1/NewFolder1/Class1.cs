五．生物质能发电技术 
以生物质能为能源发电，是生物质能规模化利用的主要形式。 
从能量转换的观点和动力系统的构成来看，他们都与火力发电基本相同，将生物质原料直接或处理后送入锅炉燃烧把化学能转化为热能，以蒸汽做工质进入汽轮机驱动发电机发电。 
（一）垃圾焚烧发电工艺流程【考过简答题】 
垃圾运输车将垃圾运至电站经地磅称重后，在卸料门卸到垃圾储坑中。垃圾在坑内发酵脱水后，由垃圾吊车将垃圾送入给料器，并进入焚烧炉内焚烧。送风机从垃圾储坑中吸入空气，使垃圾储坑保持负压避免坑中臭气外漏，空气经过空气预热器后作为锅炉的一次风和二次风。燃烧完全的灰渣送到灰渣处理系统，灰渣处理过程的污水和电厂内部的其他的污水一起送到污水处理厂进行无害化处理；燃烧产生的热量和烟气经过余热锅炉进行能量回收，产
生的蒸汽送气轮机发电，锅炉出来的烟气经烟气处理系统除尘、除酸后由烟囱排入大气；除尘设备收集的飞灰和灰渣送到场外进行处理。 
