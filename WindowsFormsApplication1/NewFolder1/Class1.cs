第八章  生物质能 
    生物质能即是指直接或间接地通过绿色植物的光合作用，把太阳能转化为化学能后固定和储藏在生物质内部的能量。 
生物质能是仅次于煤炭、石油和天然气而居于世界能源消费总量第四位的能源，在整个能源系统中占有重要地位。 
1.生物质能分类及特点 
    生物质主要有木质、非木质和动物粪便。 
（一） 分类 


木质（森林、农业的种植物、森林之外的树木）、非木质（农作物、庄稼的废弃物、加工过程的废弃物）、动物粪便。 
（二）生物质能特点【简答题】 
1、提供能源的同时不增加二氧化碳排放量。 
2、硫、氮和灰分含量少，转化过程中可以减少硫化物、氮化物和粉尘的排放。 
3、挥发分含量大，含氧量高，有利于生物质的着火和燃烧。 
4、水分含量大，影响着火和燃烧的稳定性。燃烧时造成大量能量损失，且可能引起燃料储存问题。 
5、单位质量生物质的热值低。 
6、生物质分布分散能量密度低，收集运输和预处理过程费用高。 
7、生物质具有可再生性、原料具有多样性和广泛性，资源开发潜力大。 
