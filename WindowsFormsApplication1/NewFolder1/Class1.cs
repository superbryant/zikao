核电站对环境的影响主要是指运行中对环境造成的辐射或非辐射影响。正常运行时的辐射环境影响主要来源于气液态流出物的排放和放射性固体废物的储存和处置，非辐射环境影响主要是废热、废水排放、与火电厂相似。【简答题】【1704专简答】 
核电站在设计上采取了多道屏蔽的防护设施。 
核电站防护措施：第一道安全屏障是核燃料本身，第二道安全屏障是燃料元件的包壳，第三道安全屏障是反应堆的压力壳，第四道安全屏障是反应堆的安全壳。【简答题】【1704专简答】 
环境影响：核电站正常运行并严格对放射性废物处理时，对环境的影响是极其微小的。核电站的放射性核废料是主要的环境问题。目前常见的高放射性核废物采用地质深埋的方法。到目前为止，世界上仍然没有任何国家找到安全、永久处理高放射性核废料的办法。 
八．核电站与燃煤火力发电的区别【可能简答题】 
核电站反应堆内裂变过程产生的热量，约有2/3排放到附近环境中，高于同功率的火厂，不过这种影响也十分有限。和其他能源工业相比，核电站的污染物排放量相对极少。比如，化石燃料电厂因向大气排放大量的CO2而成为全球温室气体排放的最主要来源，而核电站自身却没有温室气体排放。核电运行成本比火电成本低1/3-1/2，应该说核能发电是一种比较安全、清洁、经济的能源。 
第五章  电能 
一．电力系统基本知识 
1.电力系统的定义及基本参量 
（一） 定义 


由生产、变换、传送、分配、消耗电能的电器设备联系在一起组成的统一整体就是电力系统。 
（二） 基本参量【简答题】 

. 总装机容量（系统中所有发电机组额定有功功率的总和，以MW计）； 

. 年发电量（系统中所有发电机组全年所发电能的总和，以MW·h计）； 

. 最大负荷（指规定时间内电力系统总有功功率负荷的最大值，以MW计）； 


. 年用电量（接在系统上所有用户全年所用电能的总和，以MW·h计）； 

. 额定频率（我国规定的交流电力系统的额定功率为50Hz）； 

. 最高电压等级（指电力系统中最高电压等级电力线路的额定电压，以kV计）。 


2.电力系统构成 
电力系统是由发、变、输、配、用电设备等和相应的辅助系统，按规定的技术和经济要求组成的一个统一系统。 
简单电力系统组成：发电机、升压变压器、输电线路、降压变压器、电力负荷。【简答题】在电力系统中，除去发电机，用电设备之外的部分，称为电力网，简称电网。 
电力网按其本身结构，又可分为开环电力网和闭环电力网。 
3.电力系统特点【简答题】 
（一）电能不能大量储存（电能的产生、输送、分配、消费、使用实际上是同时进行的，每时每刻系统中发电机发出的电能必须等于该时刻用户使用的电能与电网中的电能损耗之和）。 
（二）过渡过程非常迅速（传输速度近似于光速）。 
（三）与国民经济和全社会密切相关。 
4.电力系统运行的基本要求 
（一） 评价电力系统的性能指标 


评价电力系统的性能指标是安全可靠、电能质量、经济性和环保。 
（二） 电力系统应满足的基本要求【简答题】 


1、保证可靠地持续供电。 
2、保证良好的电能质量。 
电能质量的指标主要有供电电压、频率、波形。电力系统的电压和频率正常是保证电能质量的两大基本指标。电压质量和频率质量一般以偏离额定值大小来衡量，电压偏移不应超过额定电压的±5%，电力系统运行规定频率偏移不超过±0.2Hz。三相电力系统中三相不对称的程度称为三相不平衡度，电力系统公共连接点正常电压不平衡度允许值为2%，短时不得超过4%。正弦交流电的波形质量一般以谐波畸变率衡量。 
3、努力提高电力系统运行的经济性（主要反映在降低发电厂的能源消耗、厂用电率和电力网的电能损耗等指标上）。 
4、环境和生态保护。 
（三）电力变压器的主要作用除了升高或降低电压外，还能起到将不同电压等级的电网相联系的作用。 
（四）换流设备是直流输电系统的关键部分。 
（五）直流输电的适用范围主要有：【简答题】【只本科考】 
1、远距离大功率输电。 
2、用海底电缆隔海输电或用地下电缆向负荷密度很高的大城市供电。 
3、作为系统间联络线，用来实现不同步或不同频率的两个交流系统的互联。 
4、用于限制互联系统的短路容量。 
5.电力网络层次划分 
（一）一般电力网的划分层次 
一般电力网的划分层次：一级输电网络、二级输电网络、高压配电网和低压配电网等层次。 
输电网络一般是由电压为220kV 及以上的主干电力线路组成，二级输电网络的电压一般为110kV～220kV。配电网是向中等用户和小用户供电的网络，6kV～35kV 的称高压配电网，1kV 以下的称低压配电网。低压配电网：380/220V。 
（二）电力网的电压等级 
一般说来，电力网的主干线和相邻电网的联络线多采用500kV、330kV 和220kV 等级；二级输电网采用220kV、110kV 等级；35kV 既用于城市和农村的配电网，也用于大工业企业的内部网。 
我国规定的电力网标准电压等级主要有：3、6、10、35、60、110、220、330、500kV。 
二.电力系统的负荷及其计算 
1.电力负荷的分级 
电力系统的负荷，按其对供电可靠性的要求及中断供电所造成的人身伤亡、设备损坏，在政治、经济上所造成的损失和程度可分为以下三级： 
（一）一级负荷（突然中断供电将造成人身伤亡；或在经济上造成重大损失；或在政治上造成重大影响或引起公共场所秩序严重混乱等电能用户）。 
（二）二级负荷（突然中断供电，将在政治上、经济上造成较大损失的负荷，如中断停电将造成主要设备损坏、大量产品报废、连续生产过程被打乱需较长时间才能恢复、重点企业大量减产的电能用户）。 
（三）三级负荷（一般电力负荷，即所有不属于一、二级负荷者，一般由单回线路供电即可）。 
