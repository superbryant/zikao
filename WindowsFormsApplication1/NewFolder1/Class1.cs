一．水能资源 
水能是蕴藏于河川和海洋水体中的位能和动能，是洁净的一次能源、用之不竭的再生能源。 
水能是自然界广泛存在的一次能源。它可以通过水电站方便地转换为优质的二次能源——电能，所以通常所说的“水电”既是常规能源，又是可再生资源，而且水力发电对环境无污染，因此水能被认为是世界上众多能源中永不枯竭的优质能源。 
我国国土辽阔，河流众多，径流丰沛，落差巨大，蕴藏着丰富的水能资源。据估计，我国河流水能资源的理论蕴藏量为6.76×108kW，年发电量为59200×108kW·h，不管是水能资源的理论蕴藏量还是可能开发的水能资源，中国在世界各国中均居第一位。 
水能利用是水资源综合利用的重要环节。水力发电、农田灌溉、治涝防洪、水路航运、水产养殖、工农业用水及民用给水、旅游与环境保护等都与水资源密切相关。 
河流的开发应根据河段的地形、地质及水文等自然条件采用不同的水能利用方式：【简答题】 
堤坝式是在河流地形地质条件适宜的地方建拦河筑坝，抬高上游河段的水位，形成水库，与下游天然水位形成落差，既可引水发电。引水式是在坡度较陡的河段及河湾两端河床高程相差较大的地方利用引水道引水，与天然水面形成落差，用以发电。混合式电站水头的取得，一部分是利用拦河筑坝提高水位，一部分是利用引水道集中水头。梯级开发的方式通常用于某河段由于落差过大或淹没损失过多，集中开发方式在技术、经济上不合算时，才根据具体条件进行分级开发。 
水库径流调节是水能利用中的一个重要方面，它是利用水库控制和调节径流，在时间上进行重新分配，以满足国民经济各用水部门的需要。 
