八.洁净煤技术 
（一）洁净煤技术是旨在减少污染和提高效益的煤炭加工、燃烧、转换和污染控制等新技术的总称。 
（二）洁净煤技术按其生产和利用的过程大致划分为四类： 
洁净加工技术、高效洁净转化技术、高效洁净燃烧与发电技术、燃煤污染排放治理技术等。【简答题】 
（三）洁净煤技术的四个领域和14项技术 
1、煤炭加工：洗选煤、型煤、水煤浆技术。 
2、煤炭转化：气化、液化、燃料电池技术。 
3、煤炭高效洁净燃烧：循环流化床发电技术、增压（加压）流化床发电技术、整体煤气化联合循环发电技术。 
4、污染物排放控制与废弃物处理：烟气净化、电厂粉煤灰综合利用、煤层气开发利用、煤矸石和煤泥水综合利用、工业锅炉和窑炉技术改造。 
九.洗选煤【只本科考】 
（一）燃烧前的处理主要是洗选煤、型煤和水煤浆三项技术措施。洗选煤的目的是降低原煤中的灰分、硫分等杂质的含量，并将原煤加工成质量均匀、能适应用户需要的不同品种及规格的商品煤。 
（二）常用的脱硫方法： 
1、热解法脱硫； 
2、碱法脱硫； 
3、气体脱硫； 
4、氧化脱硫。 
十．型煤【只本科考】 
型煤是将煤粉或低品位煤加工成一定形状、尺寸和有一定理化性能的煤制品。型煤一般需加黏结剂。 
特点：型煤是各种洁净煤技术中投资小、见效快、适宜普遍推广的技术。 
十一．水煤浆【只本科考】 
水浆煤是将煤研磨成一定粒度，与水按一定质量比例混合，再添加少量的添加剂，经过强力搅拌而形成的煤水两相流浆体。它不仅保持了煤炭原有的物理特性，而且具有良好的流动性和稳定性，能够将燃料油那样易于装卸、贮存、管道输送及雾化燃烧，可以广泛应用于工业窑炉、工业锅炉及电站锅炉中。 
特点：水煤浆为多孔隙的煤粉和水的固液混合物，所以可以像燃料油那样通过管道输送，而且比燃料油便宜，水煤浆在制造过程中可以净化处理，燃烧效率高，烟尘排放低于燃烧散煤。水煤浆中煤粉颗粒的质量浓度为65%—70%，含水30%—35%，所以水煤浆含量以煤为主，水为辅。 
制备方法：（一）干法；（二）湿法；（三）混合法。 
十二.煤的气化【只本科考】 
煤的气化和液化也是清洁煤技术的重要组成部分。煤的气化和液化不但能解决直接燃烧时燃烧效率低、燃烧稳定性差的缺陷，而且能够极大地改善煤直接燃烧所造成的环境污染。用煤提炼的气体燃料，称为煤制气或煤气，也成瓦斯气。煤按制气工艺不同，煤气可分为干馏煤气和气化煤气。 
十三.洁净煤发电技术【只本科考】 
洁净煤发电技术主要有： 
（一）循环流化床燃烧（二）增压流化床燃烧（三）整体煤气化联合循环（四）高效低污染的超临界凝汽式发电  
十四.工业锅炉 
锅炉已成为现代社会生产和人民生活不可缺少的特种重要设备。 
锅炉分类： 
（一）按用途分类 
1、工业锅炉。 
2、电站锅炉【用于发电，为高参数、大容量锅炉】。 
3、船用锅炉。 
4、机车锅炉 
（二）按锅炉出口工质压力分类 
1、常压热水锅炉。在任何情况下，锅筒水位线处的表压力为零的锅炉。 
2、低压锅炉。锅炉额定出口工质压力（表压力） p<=2.5MPa 的锅炉。 
3、中压锅炉。锅炉额定出口工质压力（表压力） P =3.82MPa 的锅炉。 
4、高压锅炉。锅炉额定出口工质压力（表压力） p =9.8MPa 的锅炉。 
5、超高压锅炉。锅炉额定出口工质压力（表压力） P = 13.7MPa 的锅炉。 
6、亚临界锅炉。锅炉额定出口工质压力（表压力） P = 16.7MPa 的锅炉。 
7、超临界锅炉。锅炉额定出口工质压力（表压力） P >22.1MPa 的锅炉。 
（三）按所用燃料或能源分类 
1、燃煤锅炉。2、燃油锅炉。3、水煤浆锅炉。4、燃气锅炉。5、混合燃料锅炉。6、肥料锅炉。7、余热锅炉。8、其他能源锅炉。 
（四）按燃烧方式分：（1）火床燃烧（层燃）锅炉；（2）火室燃烧（悬浮燃烧）锅炉；（3）
流化床燃烧（沸腾燃烧）锅炉；（4）旋风炉燃烧锅炉。 
（五）按通风方式分：（1）自然通风锅炉；（2）机械送风锅炉；（3）机械引风锅炉；（4）平衡通风锅炉。 
（六）按炉膛烟气压力分类 
1、负压燃烧锅炉。2、微正压燃烧锅炉。3、增压燃烧锅炉。 
（七）按循环方式分类 
1、自然循环锅炉。2、强制循环锅炉。3、直流锅炉。 
（八）按锅炉结构分类 
1、锅壳锅炉：具有锅壳，容纳水、汽，锅壳内布置烟管受热面。燃烧室布置在锅壳内部的锅炉，称为内燃锅炉；燃烧室布置在锅壳外部的锅炉，称为外燃锅炉。 
2、水管锅炉：受热面布置在炉墙围护结构空间内，水、汽、汽水混合物等工质在管内流动受热、高温烟气在管外冲刷放热的锅炉。 
（九）按锅炉布置形式分类 
1、锅筒纵置式锅炉：锅筒纵向中心线与锅炉前后中心线平行的锅炉。 
2、锅筒横置式锅炉：锅筒纵向中心线与锅炉前后中心线垂直的锅炉。 
（十）按锅炉出厂形式分类 
1、快装锅炉：锅炉本体整装出厂的锅炉。 
2、组装锅炉：锅炉本体出场时，制造成若干个组合件，在安装现场拼装成锅炉整体，称为组装锅炉。 
3、散装锅炉：锅炉本体出场时，制造成大量的零件和部件，在安装地点按锅炉厂设计图样进行安装，形成锅炉整体，称为散装锅炉。 
