热水锅炉： 
（一） 热水锅炉 


单纯生产和供应各种参数热水的锅炉称为热水锅炉。 
与蒸汽锅炉比较，其根本区别在于热水锅炉内工质不发生相变。 
热水锅炉的特点【简答题】： 
（1）结构简单；（2）安全性较高；（3）节省燃料；（4）钢材消耗量较少；（5）运行操作方便。 
按锅内工质温度高低分类（可以分为低温热水锅炉及高温热水锅炉两类。我国以120℃为分界温度，即出水温度高于120℃的为高温热水锅炉，出水温度在120℃以下的为低温热水锅炉）。 
水煤浆锅炉： 
（一） 水煤浆锅炉 


水煤浆的制备以浮选精煤为原料，经脱水、脱灰、磨制，加添加剂后与水混合成浆。 
水煤浆锅炉是指使用水煤浆作为燃料的锅炉，水煤浆是由65%-70%的煤粉、30%-35%的水和少量的添加剂混合而成。在我国丰富的煤炭资源保障下，水煤炭也成为替代油、气等资源的最基础、最经济的洁净能源。 
流化悬浮高效洁净燃烧：有效的减少氮氧化物和二氧化硫的排放。 
（二）水煤浆锅炉的优缺点 
1、优点【1804本简答题】 
（1）有利于节约资源及生态和环境保护。 
（2）能提高煤炭利用率。 
（3）可以解决能源的运输问题。 
（4）节约土地。 
（5）运行成本低，自动化程度高。 
2、缺点 
（1）锅炉负荷不易调节。 
（2）运行稳定性差。 
（3）结焦和积灰。 
（4）燃烧器喷嘴的使用寿命较短。 
有机热载体锅炉： 
以有机液体作为热载体的锅炉，称为有机热载体锅炉。有机热载体锅炉分为矿物油型和合成型两大类。有机热载体的质量及性能指标有最高使用温度、黏度、闪点、残炭、酸值等。 
性能好的有机热载体的粘度小、闪点高、残碳少、酸值低。 
有些工业生产过程需要很高的加热温度，但并不需要很高的压力。因此，采用能获得高温度而无需很高压力的热载体以提高加热温度就具有很大的工程经济价值。导热油具有“高温低压”和较低教度的工作特性，恰好适应这一要求。在常压下导热油的初馏点比水的蒸发温度要高出数倍。高温导热泊在300℃的条件下仍不汽化而保持常压，在此温度下饱和水蒸气的压力已高达8.5MPa。因此，在高温传热条件下，用导热油代替水蒸气为热载体，就能以低压供热系统取代高压供热系统，可降低设备和管道投资，并能提高锅炉运行的安全性和可靠性。 
2、有机热载体锅炉及其供热循环系统的特点【论述题】 
（1）获得低压高温热介质。 
（2）无冷凝排放热损失，热效率高。 
（3）水处理设备及系统可以简化或省略。 
（4）有机热载体锅炉房要求防护距离小，爆炸危险性小。 
（5） 有机热载体锅炉尾部可设置热管式蒸汽余热回收装置，充分利用烟气热量，大大提高热能利用效率。 
（6）需考虑有机热载体吸、放热和温度升降对体积的变化，在整个系统中应设置补偿措施。 
（7）需严格控制有机热载体内空气、水分和其他挥发分的含量。 
（8）要保证结构的严密性，不允许有机热载体发生泄漏，以免发生事故；要防止有机热载体凝冻。 
十五.煤气发生炉 
煤气发生炉的概念 
生产发生炉煤气的设备。发生炉煤气属于固体燃料经过气体的一种热加工过程。 
煤气发生炉的类型【简答题】 
按气化剂不同分为三种煤气类型：空气煤气-以空气为气化剂；水煤气-以水蒸气为气化剂；混合煤气-以空气和水蒸气为气化剂。 
按生产发生炉煤气的设备，分为单段式煤气炉和两段式煤气炉两大类。 
煤气发生炉的分层： 
在一般的单段式煤气发生炉中，煤由上而下、气化剂则是由下而上地进行逆流运动，它们之间发生化学反应和热量交换。发生炉内部由下向上依次分为六层：即灰层、氧化层（又
叫燃烧层）、还原层、干馏层、干燥层、空层。【简答题】 
. 灰层又称灰渣层，是固态物料由上向下移动的最后一层，是煤炭燃烧与汽化后的混合物。 

. 氧化层又称为燃烧层或火层。 

. 还原层：在氧化层的上面是还原层。在还原层中，二氧化碳和水蒸气被碳还原成一氧化碳和氢气。 

. 干馏层位于还原层的上部，由还原层上升的气体随着热量被消耗，其温度逐渐下降。 

. 干燥层位于干馏层上面，也是燃料的面层，上升的热煤气与刚入炉的燃料在这层相遇，进行热交换，燃料中的水分受热蒸发。 

. 空层即燃烧层上部，炉体内的自由区，其主要作用是汇集煤气。 


从节能环保角度理解单段炉和两段炉的区别【简答题】： 
单段炉：产生的冷煤气对水的污染严重，特别是在净化过程中煤气直接用水来洗涤和降温，把煤气中大量的杂质带出，产生的酚水对环境污染严重。而且气化强度比较低。 
两段炉：净化采用间接冷却，水和煤气不直接接触，避免了对水的污染，更好的体现了两段炉的优越性。气化的更完全。灰渣含碳率非常低，一般在12%左右，而单段炉在20%左右。 
