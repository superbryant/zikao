（三）柴油的质量指标 
柴油分重柴油和轻柴油。重柴油一般用作柴油机燃料。轻柴油是由石油的各种直馏柴油馏分、催化柴油馏分和混有热化柴油馏分等制成。（柴油的粘度小，流动性好，雾化过程中一般不需要加热，含硫量较小，对环境污染也小，但容易挥发，火灾危险性大，小容量锅炉普遍采用轻柴油作为燃料。） 
