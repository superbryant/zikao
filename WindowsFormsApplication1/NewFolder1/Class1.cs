（一）水管锅炉：主要特点是烟气在受热面管子外流动放热，水或汽水混合物在管子内流动吸热。对水质要求较高，生产时需要更大型、更先进的焊接、加工设备。 
水管锅炉比锅壳锅炉具有的优势： 
1、能适应锅炉参数（工质温度和压力）提高的要求。 
2、各种受热面的布置比较灵活。 
3、有更高的安全裕度。 
