第一章  煤炭 
一.煤是最丰富的化石原料，它是原始植物经过复杂的生物化学作用和物理化学作用转变而成的。随着煤化程度增高，煤中含碳量增加，氢和氧的含量减少，容量增大。 
二.煤的元素的组成 
（一）煤是由有机物质和无机物质混合组成的。煤中有机物质主要由碳（C）、氢(H)、氧(O)、氮(N)四种元素组成。煤中无机物质主要由硫（S）、磷（P）以及其他元素组成。 
（二）每种元素的作用 
1、碳是煤中有机物质的主导成分，也是最主要的可燃物质。煤中碳含量随煤变质程度的加深而增加。 
2、氢也是煤中重要的可燃物质。煤炭中氢含量一般是随煤炭的变质程度加深而减少。氢的发热量最高。 
3、氧是煤中不可燃的元素。煤的氧含量也随变质程度的加深而减少。 
4、硫是煤中的有害物质。分为无机硫和有机硫。 
前者多以矿物杂质的形式存在于煤中，可进一步按所属的化合物类型分为硫化物硫和硫酸盐硫。有机硫则是直接结合于有机母体中的硫。煤中有机硫主要由硫醇、硫化物以及二硫化物三部分组成。近年来，随着分析技术的进步，还在煤中检出了硫的另一种存在形态，即单质硫。 
5、磷是煤中有害成分。磷在煤中的含量一般不超过1%。一般规定炼焦用煤的磷含量不应超过0.01% 。炼焦时煤中的磷可全部转人焦炭之中，炼铁时焦炭中的磷又转人生铁中，这不仅增加溶剂和焦炭的消耗量，降低高炉生产率，还严重影响生铁的质量，使其发脆。 
6、煤中氮含量较少，仅为1%～3%。主要来自成煤植物。 
3.常用的煤质指标有：水分、灰分、挥发分和发热量。 
（一）水分：是煤中不可燃的成分。其来源有三种，即外部水分、内部水分和化合水分。煤中水分含量的多少取决于煤的内部结构和外界条件。 
（二）灰分：指煤完全燃烧后其中矿物质的固体残余物。 
灰分的来源：一是形成煤的植物本身的矿物质和成煤过程中进入的外来矿物杂质；二是开采运输过程中掺杂进来的灰、沙、土等矿物质。煤的灰分几乎对煤的燃烧、加工、利用的全部过程都有不利影响。灰分含量高，不仅使煤发热量减少，而且影响煤的着火和燃烧。灰分每增加1% ，燃料消耗即增加1% 。由于燃烧的烟气中飞灰浓度大，使受热面易受污染而影响传热，降低效率，同时使受热面易受磨损而减少寿命。为了保护大气环境，控制排烟中烟尘的排放浓度，还必须对烟气中尘粒进行除尘处理。【1804简答题】 
（三）挥发分：在隔绝空气的条件下，将煤加热到850℃左右，从煤中有机物质分解出来的液体和气体产物称为挥发分。（挥发分是表征煤炭性质的主要指标）。煤的挥发分常随煤的变质程度而有规律地变化，变质程度越大的煤，挥发分越少。 
（四）发热量：单位质量煤完全燃烧时所放出的热量称为煤的发热量。煤的发热量分为高位发热量和低位发热量（也叫净热）。【单位KJ.kg-1】 
煤的可燃成分是碳、氢、硫，相应的燃烧产物是CO2（不完全燃烧时还有CO）、水、SO2（进一步氧化生成SO3）。 
三.煤的分类：通常最简单的分类方法是根据煤中干燥无灰基挥发分含量(Vdaf)分成褐煤、烟煤和无烟煤。 
褐煤是煤中埋藏年代最短、碳化程度最低的一类。 
无烟煤是煤化程度最高的煤种，干燥无灰基挥发分含量最少，低位发热量最高；褐煤干燥无灰基挥发分含量最多，低位发热量最低。 
四.焦炭是用炼焦用煤焦炉炭化室内隔绝空气加热到1300℃以上，使煤进行炭化获得的产物。 
五.火电厂生产过程 
利用煤、石油、天然气等自然界蕴藏量及其丰富的化石燃料的化学能发电称为火力发电。火力发电厂简称火电厂。我国电源构成是以火力发电为主。燃煤火电厂是我国目前电能生产的主要方式。 
锅炉设备是火力发电厂的主要热力设备之一。其作用是：燃料在炉膛内燃烧将其化学能转变为烟气热能；烟气热能加热给水，水经过预热、汽化、过热三个阶段成为具有一定压力、温度的过热蒸汽。 
（一）火电厂按燃料不同、原动机不同有不同的类型。按照原动机不同可分为汽轮机电厂、燃气轮机电厂、内燃机电厂、蒸汽-燃气轮机联合循环电厂。水、蒸汽是把热能转化成机械能的重要工质。在火力发电厂中，锅炉将燃料的化学能转化为蒸汽热能，汽轮机将蒸汽热能转化为机械能，发电机将机械能转化为电能。锅炉、汽轮机、发电机并称火力发电厂的三大主机。 
（二）汽轮机设备组成：汽轮机本体和汽轮机辅助设备两部分组成。 
主要结构：由静止、转动两大部分组成，分别是定子和转子。 
定子包括：汽缸、喷嘴、隔板、汽封、轴承等； 
转子包括：主轴、叶轮、动叶、联轴器等。 
（三）汽轮发电机的工作原理 
按电磁感应定律，导线切割磁力线感应电动势，这是发电机的基本工作原理。 
（四）火电厂生产过程【过去考过论述题】 
从能量转换观点分析，其基本过程都是燃料的化学能—热能—机械能—电能。原煤用火车或轮船运入发电厂储煤场，经过碎煤设备破碎后，由皮带运输机送人锅炉房内的原煤斗。 
煤从煤斗落入磨煤机，在磨煤机中研磨成煤粉，同时送入热空气来干燥和输送煤粉磨制好的煤粉，经粗粉分离器得以分离，分离出来的细粉分离器除去部分不合格的煤粉后进入旋风分离器，在其中空气和煤粉得以分离，分离出来的细粉进入煤粉仓。煤粉由给煤机送入输粉管，而旋风分离器中的空气则由排粉机抽出。煤粉和空气在输粉管混合后，由喷燃器喷入炉膛进行燃烧。燃烧放出的热量一部分被燃烧室四周的水冷壁吸收，一部分加热燃烧室顶部和烟道入口处的过热器中的蒸汽，余下的热量则被烟气携带穿过省煤器、空气预热器把部分热量传递给这两个设备内的水和空气。由送风机送来的空气，在进入炉膛之前，先在空气预热器中接受排烟预热，以减少排烟热损失，并提高空气温度，改善燃烧过程。炉膛内的燃烧产物—高温烟气，在引风机的拔风作用下，沿着锅炉本体倒U 形烟道依次流过炉膛、过热器、省煤器和空气预热器，将热量逐步传递给水、蒸汽和空气。降温后的烟气流入除尘器进行净化，净化除尘后的烟气则被引风机抽出，排入大气，将燃料的化学能转换为热能。燃烧时生成的灰渣和由除尘器收集下来的细灰，用水冲进冲灰沟排至厂外灰场。 
水、蒸汽是把热能转化成机械能的重要工质。净化后的给水，先送进省煤器预热，继而进入汽包后再入水冷壁管中吸收燃烧室的热能后蒸发成蒸汽。 
六.电厂锅炉及辅助设备【只本科考】 
（一）电厂锅炉的概述 
1、锅炉是将燃烧内的潜在能量经过燃烧释放热能或利用其他能源释放的能量工作的设备。锅炉设备是火力发电厂的主要热力设备之一。 
2、锅炉的作用： 
（1）将水变成蒸汽或过热蒸汽 
（2）将水加热变成一定温度的热水 
（3）将有机热载体加热到一定温度而输出热能的设备。 
3、锅炉、汽轮机中将热能转换为机械能的媒介——给水和蒸汽，称为工质。按照工质在锅炉中的受热流动的特点，分为自然循环锅炉、强迫循环锅炉和直流锅炉三类。 
（二）锅炉由锅炉本体和辅助设备两大部分组成。 
锅炉本体实际上就是一台庞大的热交换器，由“锅”和“炉”两部分组成。 
（1）锅是指锅炉的汽水系统，完成水变为过热蒸汽的吸热过程（包含省煤器、汽包、水冷壁、过热器、再热器）。 
（2）炉是指锅炉的燃烧系统，完成煤的燃烧放热过程（包含炉膛、燃烧器、烟道、风道、空气预热器）。 
省煤器是利用锅炉炉膛后尾部的低温烟气加热给水的受热面，用来完成给水吸热的预热阶段，可以起到降低排烟温度、节省燃料、提高锅炉效率的作用。 
空气预热器是利用锅炉尾部的低温烟气热量来加热助燃用空气的热交换器。 
