第六章  太阳能 
太阳辐射的总功率为3.8×1023kW,到达地面的太阳辐射总功率为1.7×l011kW。太阳能是可再生能源，它资源丰富、取之不尽、遍布世界和天空、无需开采和运输、清洁而无任何污染。 
一．太阳能光伏发电 
太阳能光伏发电是太阳能利用的一种重要形式，是采用太阳能电池将光能转换为电能的发电方式。太阳电池的基本原理为半导体的光伏效应。 
1.光伏发电技术【简答题】 
（一）晶体硅太阳电池技术(当前太阳电能光伏电池的主流，最高理论效率可达25%)。 
（二）薄膜太阳电池技术（材料有三种：非晶硅、铜铟硒和碲化镉。是一种很有前景的技术）。 
（三）聚光太阳电池技术。 
（四）染料敏化太阳电池。 
2.光伏发电系统 
光伏系统是采用太阳电池将太阳辐射能直接转换成电能的完整的发电系统。它是由太阳电池方阵、控制器、蓄电池组、直流/交流逆变器等部分组成。 
太阳电池组件及方阵：太阳电池单体是光电转换的最小单元，尺寸一般为4~200cm2 不等。工作电压约为0.5V，工作电流为20-25mA/cm2。一个组件中，太阳电池的标准数量是36片，一个太阳电池组件大约能产生17V的电压，正好能为一个额定电压为12V的蓄电池进行有效充电。 
储能蓄电池是通过充电将电能转换为化学能贮存起来，蓄电池是光伏电站的贮能装置。 
充放电控制器的作用：1.高压断开和恢复功能；2.欠电压告警和恢复功能；3.低压断开和恢复功能；4.保护功能；5.温度补偿功能；6.其他功能。 
太阳能发电系统中，使用直流负载的情况下，不需要的设备是逆变器。逆变器的功能是将直流电转换为交流电。 
3.光伏发电系统的应用【只本科考】【简答题】 
光伏发电可以分为独立光伏发电系统和并网光伏发电系统。其中通信和工业应用主要有：微波中继站，光缆通信系统，无线寻呼台站等。 
农村和边远地区主要应用于：独立光伏电站，小型风光互补发电系统等。 
对于并网光伏发电系统来说，主要用于城市与建筑结合的并网光伏发电系统和大型荒漠/开阔地光伏电站。 
二．太阳能热发电 
太阳能热发电是指利用聚光器将低密度的太阳能汇聚到焦斑处，使其生成高密度的能量，然后由工作流体将其转化成热能，再利用热能发电的方式。太阳能热发电与火电站相比，其热力循环部分是相同的，区别在于火电站使用化石燃料获得热能，而太阳能热发电则利用太阳能产生热能。 
塔式、槽式、碟式、菲涅尔式、太阳能烟囱以及太阳池发电等几种。其中已经达到商业化应用水平的主要是聚焦型的塔式、槽式、碟式三种方式。前两种发电传热工质采用水，后一种采用氢气或者氦气。 
塔式热发电系统总效率13%左右，集光-吸热部分效率70%左右。 
   塔式太阳能热发电系统将吸热器置于接收塔的顶部；槽式和碟式将太阳光聚焦到镜面焦点处，在该处防止吸热管吸收热能。 
