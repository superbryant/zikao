平板集热器一般由吸热板、玻璃盖板、保温层和外壳四个部分组成。 
优点：（1）使用寿命长；（2）具有较好的承压性能；（3）适用于双循环的太阳能热水器；（4）故障率低 
局限性：自然循环系统的平板型太阳能热水器的工作温度要求在0℃以上。冬季时，集热器中的水易结冰，只能在不结冰地区使用。另外，我国的平板型集热器的生产和研发投入少，平板型集热器吸热涂层的生产尚依赖进口，规模化生产能力有待进一步的提高，制约了我国平板型太阳能热水器的推广和应用。 
